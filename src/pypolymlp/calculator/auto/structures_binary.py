    """Return HCP structure."""
    axis = np.zeros((3, 3))
    a = 3.0 * scale
    axis[0, 0] = a
    axis[0, 1] = -a / 2.0
    axis[1, 1] = a * np.sqrt(3) / 2.0
    axis[2, 2] = a * np.sqrt(8.0 / 3.0)
