def set_structure(
    axis: np.ndarray,
    positions: np.ndarray,
    n_atoms: np.ndarray,
    element_strings: tuple,
):
    """Set PolymlpStructure instance."""
    types = np.array([i for i, n in enumerate(n_atoms) for _ in range(n)])
    elements = [ele for n, ele in zip(n_atoms, element_strings) for _ in range(n)]
    st = PolymlpStructure(
        axis=axis,
        positions=positions,
        n_atoms=n_atoms,
        types=types,
        elements=elements,
    )
    return st


